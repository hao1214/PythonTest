'''
《百錢百雞》問題
公雞5元一隻，母雞3元一隻，小雞1元三隻，用100塊錢買一百隻雞，問公雞、母雞、小雞各有多少只？
'''

###
#  a + b + c = 100
# 5a + 3b + 1/3 * c =100
#  c = 100 -a - b
# a 最大值:20 , b最大值:33, c最大值:33

for a in range(20):
    for b in range(33):
        c = 100 - a - b
        if 5*a + 3*b + 1/3 * c == 100:
            print(f'公雞: {a}只, 母雞: {b}只, 小雞: {c}只')
